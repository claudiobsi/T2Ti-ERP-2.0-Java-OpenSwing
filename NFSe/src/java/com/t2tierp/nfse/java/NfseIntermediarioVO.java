package com.t2tierp.nfse.java;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;
import org.openswing.swing.message.receive.java.ValueObjectImpl;


@Entity
@Table(name = "NFSE_INTERMEDIARIO")
public class NfseIntermediarioVO extends ValueObjectImpl implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "ID")
    private Integer id;
    @Column(name = "CNPJ")
    private String cnpj;
    @Column(name = "RAZAO")
    private String razao;
    @Column(name = "INSCRICAO_MUNICIPAL")
    private String inscricaoMunicipal;
    @JoinColumn(name = "ID_NFSE_CABECALHO", referencedColumnName = "ID")
    @ManyToOne(optional = false)
    private NfseCabecalhoVO nfseCabecalho;

    public NfseIntermediarioVO() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getCnpj() {
        return cnpj;
    }

    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    public String getRazao() {
        return razao;
    }

    public void setRazao(String razao) {
        this.razao = razao;
    }

    public String getInscricaoMunicipal() {
        return inscricaoMunicipal;
    }

    public void setInscricaoMunicipal(String inscricaoMunicipal) {
        this.inscricaoMunicipal = inscricaoMunicipal;
    }

    public NfseCabecalhoVO getNfseCabecalho() {
        return nfseCabecalho;
    }

    public void setNfseCabecalho(NfseCabecalhoVO nfseCabecalho) {
        this.nfseCabecalho = nfseCabecalho;
    }


    @Override
    public String toString() {
        return "com.t2tierp.nfse.java.NfseIntermediarioVO[id=" + id + "]";
    }

}
