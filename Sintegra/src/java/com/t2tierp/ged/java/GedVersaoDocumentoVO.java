@Entity
@Table(name = "GED_VERSAO_DOCUMENTO")
public class GedVersaoDocumentoVO extends ValueObjectImpl implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "ID")
    private Integer id;
    @Column(name = "VERSAO")
    private Integer versao;
    @Temporal(TemporalType.TIMESTAMP)
    @Column(name = "DATA_HORA")
    private Date dataHora;
    @Column(name = "HASH_ARQUIVO")
    private String hashArquivo;
    @Column(name = "CAMINHO")
    private String caminho;
    @Column(name = "ACAO")
    private String acao;
    @JoinColumn(name = "ID_GED_DOCUMENTO", referencedColumnName = "ID")
    @ManyToOne(optional = false)
    private GedDocumentoVO gedDocumento;
    @JoinColumn(name = "ID_COLABORADOR", referencedColumnName = "ID")
    @ManyToOne(optional = false)
    private ColaboradorVO colaborador;

    public GedVersaoDocumentoVO() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getVersao() {
        return versao;
    }

    public void setVersao(Integer versao) {
        this.versao = versao;
    }

    public Date getDataHora() {
        return dataHora;
    }

    public void setDataHora(Date dataHora) {
        this.dataHora = dataHora;
    }

    public String getHashArquivo() {
        return hashArquivo;
    }

    public void setHashArquivo(String hashArquivo) {
        this.hashArquivo = hashArquivo;
    }

    public String getCaminho() {
        return caminho;
    }

    public void setCaminho(String caminho) {
        this.caminho = caminho;
    }

    public String getAcao() {
        return acao;
    }

    public void setAcao(String acao) {
        this.acao = acao;
    }

    public GedDocumentoVO getGedDocumento() {
        return gedDocumento;
    }

    public void setGedDocumento(GedDocumentoVO gedDocumento) {
        this.gedDocumento = gedDocumento;
    }

    public ColaboradorVO getColaborador() {
        return colaborador;
    }

    public void setColaborador(ColaboradorVO colaborador) {
        this.colaborador = colaborador;
    }


}
